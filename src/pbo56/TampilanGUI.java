    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        String nama = jTextFieldNama.getText().trim();
        String id_dokter = jTextFieldid.getText();
        String alamat = jTextFieldAlamat.getText();

        if (!nama.isEmpty() && !id_dokter.isEmpty() && !alamat.isEmpty()) {
            try {
                Connection conn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/Dokter", "postgres", "Chanhee98");
                int k = masukkanData(conn, nama, id_dokter, alamat);
                if (k > 0) {
                    tampil(conn);
                    this.peringatan("Simpan Berhasil");
                } else {
                    this.peringatan("Simpan Gagal");
                }
            } catch (SQLException ex) {
                Logger.getLogger(TampilanGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            this.peringatan("Input Data Mahasiswa Gagal");
        }

        jTextFieldNama.setText("");
        jTextFieldid.setText("");
        jTextFieldAlamat.setText("");

    }//GEN-LAST:event_jButtonSimpanActionPerformed

