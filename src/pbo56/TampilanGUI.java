    private int masukkanData(Connection conn, String nama, String id_dokter, String alamat) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("INSERT INTO identitas (nama, id_dokter, alamat) VALUES(?,?,?)");
        pst.setString(1, nama);
        pst.setString(2, id_dokter);
        pst.setString(3, alamat);
        return pst.executeUpdate();
    }

    private int perbaruiData(Connection conn, String nama, String id_dokter, String alamat) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("UPDATE identitas SET nama=?, alamat=? WHERE id_dokter=?");
        pst.setString(1, nama);
        pst.setString(2, alamat);
        pst.setString(3, id_dokter);
        return pst.executeUpdate();
    }
    
    private int hapusData(Connection conn, String id_dokter) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("DELETE FROM identitas WHERE id_dokter = ?");
        pst.setString(1, id_dokter);
        return pst.executeUpdate();
    }

    private void tampil(Connection conn) {
        dataDokter.clear();
        try {
            String sql = "select * from identitas";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                Dokter data = new Dokter();
                data.setNama(String.valueOf(rs.getObject(1)));
                data.setId_dokter(String.valueOf(rs.getObject(2)));
                data.setAlamat(String.valueOf(rs.getObject(3)));
                dataDokter.add(data);
            }
            DefaultTableModel model = (DefaultTableModel) jTableDokter.getModel();
            model.setRowCount(0);
            for (Dokter data : dataDokter) {

                Object[] baris = new Object[3];
                baris[0] = data.getNama();
                baris[1] = data.getId_dokter();
                baris[2] = data.getAlamat();

                model.addRow(baris);
            }
        } catch (SQLException ex) {
            Logger.getLogger(TampilanGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

